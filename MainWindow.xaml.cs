        private void InitializeColorChangeTimer()
        {
            // Liste des couleurs à alterner
            colors = new List<Brush>
            {
                Brushes.LightBlue,    // Exemple de couleur 1
                Brushes.White// Exemple de couleur 4
                // Ajoutez plus de couleurs si nécessaire
            };

            colorChangeTimer = new DispatcherTimer
            {
                Interval = TimeSpan.FromSeconds(2) // Change couleur toutes les 5 secondes
            };
            colorChangeTimer.Tick += OnColorChangeTimerTick;
            colorChangeTimer.Start();
        }

        private void OnColorChangeTimerTick(object sender, EventArgs e)
        {
            // Met à jour la couleur de fond du menu
            currentColorIndex = (currentColorIndex + 1) % colors.Count;
            var newColor = colors[currentColorIndex];
            GridNav.Background = newColor; // Assurez-vous que GridNav est le bon élément à modifier
        }

